"""Aggregate runner JSON files into RESULTS.md.

Reads every ``raw/<runner>.json`` and produces a Markdown table per
batch size, plus a summary row. This script is the single source of
truth for what `RESULTS.md` says — never hand-edit `RESULTS.md`.
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Dict, List, Optional

_THIS_DIR = Path(__file__).resolve().parent
if str(_THIS_DIR) not in sys.path:
    sys.path.insert(0, str(_THIS_DIR))

from schema import BatchResult, RunnerResult, load_runner_result  # noqa: E402
from workload import WORKLOAD  # noqa: E402


# Order rows in the published table consistently regardless of which
# runners completed.
RUNNER_ORDER = ["hf", "torch_compile", "vllm", "tensorrt_llm", "agnitra"]
RUNNER_LABELS = {
    "hf": "HuggingFace `transformers`",
    "torch_compile": "`torch.compile`",
    "vllm": "vLLM",
    "tensorrt_llm": "TensorRT-LLM",
    "agnitra": "**Agnitra**",
}


def _fmt(x: float, suffix: str = "") -> str:
    if not x or x != x:  # NaN-safe
        return "—"
    if x >= 100:
        return f"{x:.0f}{suffix}"
    if x >= 10:
        return f"{x:.1f}{suffix}"
    return f"{x:.2f}{suffix}"


def _batch_row(label: str, b: Optional[BatchResult]) -> str:
    if b is None or not b.e2e.samples_ms:
        return f"| {label} | — | — | — | — | — |"
    return (
        f"| {label} | {_fmt(b.ttft.p50_ms, ' ms')} | "
        f"{_fmt(b.ttft.p99_ms, ' ms')} | "
        f"{_fmt(b.throughput_tps, ' tok/s')} | "
        f"{_fmt(b.decode_tps, ' tok/s')} | "
        f"{_fmt(b.peak_memory_gb, ' GB')} |"
    )


def _table_for_batch(
    batch_size: int,
    by_runner: Dict[str, RunnerResult],
) -> str:
    header = (
        f"### Batch size {batch_size}\n\n"
        "| Runner | TTFT p50 | TTFT p99 | Throughput | Decode | Peak Mem |\n"
        "|---|---:|---:|---:|---:|---:|\n"
    )
    rows: List[str] = []
    for runner in RUNNER_ORDER:
        result = by_runner.get(runner)
        label = RUNNER_LABELS.get(runner, runner)
        if result is None:
            rows.append(f"| {label} | — | — | — | — | — |  *(no result file)*")
            continue
        if not result.success:
            err = (result.error or "no batches recorded").splitlines()[0]
            rows.append(f"| {label} | — | — | — | — | — |  *({err[:80]})*")
            continue
        match = next((b for b in result.batches if b.batch_size == batch_size), None)
        rows.append(_batch_row(label, match))
    return header + "\n".join(rows) + "\n"


def _speedup_summary(by_runner: Dict[str, RunnerResult]) -> str:
    lines = ["### Agnitra speedup vs. each baseline (throughput, batch=1)\n"]
    agnitra = by_runner.get("agnitra")
    if agnitra is None or not agnitra.success:
        return "_Agnitra runner did not produce results — speedup table omitted._\n"
    a_b1 = next((b for b in agnitra.batches
                 if b.batch_size == 1 and b.e2e.samples_ms), None)
    if a_b1 is None:
        return "_Agnitra has no batch=1 result — speedup table omitted._\n"
    lines.append("| Baseline | Baseline tok/s | Agnitra tok/s | Speedup |")
    lines.append("|---|---:|---:|---:|")
    for runner in ["hf", "torch_compile", "vllm", "tensorrt_llm"]:
        r = by_runner.get(runner)
        if r is None or not r.success:
            continue
        b = next((x for x in r.batches
                  if x.batch_size == 1 and x.e2e.samples_ms), None)
        if b is None:
            continue
        speedup = a_b1.throughput_tps / b.throughput_tps if b.throughput_tps else 0.0
        lines.append(
            f"| {RUNNER_LABELS[runner]} | {_fmt(b.throughput_tps)} | "
            f"{_fmt(a_b1.throughput_tps)} | {_fmt(speedup, 'x')} |"
        )
    return "\n".join(lines) + "\n"


def _env_block(by_runner: Dict[str, RunnerResult]) -> str:
    any_result = next(iter(by_runner.values()), None)
    if any_result is None:
        return ""
    return (
        f"- GPU: `{any_result.gpu_name}`\n"
        f"- CUDA: `{any_result.cuda_version}`\n"
        f"- PyTorch: `{any_result.torch_version}`\n"
        f"- Generated: `{any_result.timestamp_utc}`\n"
    )


def render(raw_dir: Path) -> str:
    by_runner: Dict[str, RunnerResult] = {}
    for runner in RUNNER_ORDER:
        path = raw_dir / f"{runner}.json"
        if not path.is_file():
            continue
        try:
            by_runner[runner] = load_runner_result(path)
        except Exception as exc:
            print(f"WARN: failed to parse {path}: {exc}", file=sys.stderr)

    if not by_runner:
        return (
            "# Llama-3-8B / H100 Benchmark — Results\n\n"
            "_This file is generated by `compare.py` from JSON outputs in `raw/`._\n\n"
            "**No published numbers yet.** Run `./run.sh` on an H100 to populate this\n"
            "file. Until that happens, treat any performance claim about Agnitra as\n"
            "unverified.\n\n"
            "To reproduce on an H100:\n\n"
            "```bash\n"
            "HF_TOKEN=hf_xxx ./run.sh\n"
            "```\n\n"
            "Then commit the regenerated `RESULTS.md` and the contents of `raw/` so\n"
            "reviewers can verify the math.\n\n"
            "See `README.md` in this directory for the full methodology.\n"
        )

    parts: List[str] = []
    parts.append("# Llama-3-8B / H100 Benchmark — Results\n")
    parts.append(
        "Reproducible numbers from this directory's `run.sh`. Hand edits\n"
        "are overwritten — change `compare.py` to change formatting.\n"
    )
    parts.append("## Workload\n")
    parts.append(
        f"- Model: `{WORKLOAD.model_id}`\n"
        f"- Input tokens: {WORKLOAD.input_tokens}\n"
        f"- Output tokens: {WORKLOAD.output_tokens} (greedy decode)\n"
        f"- Warmup / measure iterations: {WORKLOAD.warmup_iters} / {WORKLOAD.measure_iters}\n"
    )
    parts.append("## Results\n")
    for batch in WORKLOAD.batch_sizes:
        parts.append(_table_for_batch(batch, by_runner))
    parts.append("## Speedup\n")
    parts.append(_speedup_summary(by_runner))
    parts.append("## Environment\n")
    parts.append(_env_block(by_runner))
    parts.append(
        "## Raw data\n\n"
        "All per-runner JSON results are committed at `benchmarks/llama3_h100/raw/`.\n"
        "Re-run `python compare.py` after editing the JSON to regenerate this file.\n"
    )
    return "\n".join(parts)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--raw", type=Path, default=_THIS_DIR / "raw",
        help="Directory containing <runner>.json files.",
    )
    parser.add_argument(
        "--out", type=Path, default=_THIS_DIR / "RESULTS.md",
        help="Path to the rendered Markdown.",
    )
    args = parser.parse_args()
    args.out.write_text(render(args.raw))
    print(f"wrote {args.out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
